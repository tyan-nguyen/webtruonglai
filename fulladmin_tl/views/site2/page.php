      <h4 class="uppercase">1. LỊCH SỬ HÌNH THÀNH</h4>
     <p>Công ty TNHH Trang Trí Nội Thất Ánh Dương được thành lập từ năm 2009 với hệ thống máy móc thiết bị được nhập khẩu từ Châu Âu và Nhật Bản, cùng việc kết hợp sử dụng những nguyên liệu loại một, sản phẩm của chúng tôi luôn đạt chất lượng tốt nhất.</p>

	<p>Với cơ sở vật chất và máy móc tiên tiến, hiện đại, chúng tôi không chỉ chuyên cung cấp, sản xuất, lắp đặt các hàng inox (trắng, màu mạ PVD), gia công những tấm thép không gỉ #304, #316 với các định dạng bề mặt đa dạng: Sọc nhuyễn HL, bóng gương hay hoa văn (với hơn 100 mẫu khác nhau),  ... được áp dụng trang trí nội, ngoại thất cho các toà cao ốc, văn phòng, chung cư, khách sạn, khu nghỉ dưỡng, trung tâm thương mại... mà chúng tôi còn lò xi mạ với công nghệ mới nhất có thể mạ titan các màu sắc theo yêu cầu của khách hàng.</p>

	<p>Sản phẩm của chúng tôi rất phong phú và đa dạng: từ ốp inox mặt tiền cho các toà nhà cho tới ốp trang trí cho cột, cửa hay cầu thang máy, cầu thang cuốn, mái đón...  Bên cạnh đó,  các  sản  phẩm  đồ  nội  thất  trang  trí  kết  hợp  giữa  inox cũng là một thế mạnh của chúng tôi.</p>

	<p>Lĩnh vực kinh doanh chính:
- Sản xuất tấm ốp và gia công lắp đặt: Ốp vách thang máy, Ốp cột, Ốp sảnh - Trần, Vách CNC - Laser, Lan can, Tay vịn, Mái đón, Cửa và hệ khung inox
- Sản xuất gia công đồ nội thất: Bàn, Ghế, Đồ trang trí nội thất khác 
- Gia công cơ khí.</p>
